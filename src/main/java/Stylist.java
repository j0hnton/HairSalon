public class Stylist{


    private String Gender;;
    private String Name;
    private String Cname;


    public Stylist(String name,String gender,String cname){

         Name=name;
         Gender=gender;
         Cname=cname;


    }

    public String getgender() {
        return Gender;
    }

    public String getname() {
        return Name;
    }

    public String getcname() {
        return Cname;
    }
}



